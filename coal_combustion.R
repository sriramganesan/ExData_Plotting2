library(plyr)
library(ggplot2)

if (!file.exists("NEI_data.zip")) {
  download.file(url = "https://d396qusza40orc.cloudfront.net/exdata%2Fdata%2FNEI_data.zip", destfile = "NEI_data.zip", method = "curl")
}

if (!file.exists("Source_Classification_Code.rds")) {
  unzip("NEI_data.zip")
}

scCode <- readRDS(file = "Source_Classification_Code.rds");
permEmissions25- readRDS(file = "summarySCC_PM25.rds");

coalSources <- scCode[grepl("Fuel Comb.*Coal", scCode$EI.Sector),]

coalPermEmissions25 <- permEmissions25[permEmissions25$SCC %in% coalSources$SCC,]

emissionsByYear <- ddply(coalPermEmissions25, "year", summarise, total = sum(Emissions))

png(filename="coal_emissions_by_year.png")
qplot(x      = year,
      xlab   = "Year",
      y      = total,
      ylab   = "Total Emissions (tons)",
      data   = emissionsByYear,
      geom   = c("point", "smooth"),
      method = "loess")
dev.off()